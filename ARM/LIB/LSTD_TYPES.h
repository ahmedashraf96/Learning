/*Header file guard*/
#ifndef __LSTD_TYPES_H__
#define __LSTD_TYPES_H__

/*8-bits integer unsigned and signed data types*/
typedef unsigned char   u8_t;
typedef signed char     s8_t;

/*16-bits integer unsigned and signed data types*/
typedef unsigned short  u16_t;
typedef signed short    s16_t;

/*32-bits integer unsigned and signed data types*/
typedef unsigned long   u32_t;
typedef signed long     s32_t;

/*32-bits & 64-bits float data types*/
typedef float           f32_t;
typedef double          f64_t;

#endif /*__LSTD_TYPES_H__*/